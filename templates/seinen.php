<p>
Seinen manga is a subset of manga that is generally targeted at a 18-30 year old 
male audience, but the audience can be older with some manga aimed at businessmen 
well into their 40s. In Japanese, the word Seinen means "young man" or "young men" 
and is not suggestive of sexual matters. (The female equivalent to seinen manga is 
josei manga.)<br /><br />
It has a wide variety of art styles and more variation in subject matter, 
ranging from the avant-garde to the pornographic. Because of the emphasis on 
storyline and character development instead of action, some seinen series are often 
confused with shojo, or girls' manga. This is especially true of 
seinen comedy series such as Chobits, and Chi's Sweet Home, or seinen drama such as 
Twin Spica. Other examples of seinen manga include: Gantz, Battle Royale, 
20th Century Boys, Monster, Blame!, Ghost in the Shell, Akira, Berserk, 
Battle Angel Alita, Drifters and Elfen Lied. Rumiko Takahashi's Maison Ikkoku is 
sometimes classified as seinen, although this is disputed by some fans.<br /><br />
A common way to tell if a manga is seinen is by looking at whether or not furigana is 
used over the original kanji text: if there are furigana on all kanji, the title 
is generally aimed at a younger audience. The title of the magazine it was published 
in is also an important indicator. Usually Japanese manga magazines with the word 
"young" in the title (Weekly Young Jump for instance) are seinen. Other popular 
seinen manga magazines include Ultra Jump, Afternoon, and Big Comic.
</p>

